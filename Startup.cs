                if (!roleExist)
                {
                    //create the roles and seed them to the database: Question 2
                    roleResult = await RoleManager.CreateAsync(new IdentityRole(roleName));
                }
            }
            //Adding Admin Role
            var roleCheck = await RoleManager.RoleExistsAsync("Admin");
